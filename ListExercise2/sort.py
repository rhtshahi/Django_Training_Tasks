#---Sorting list in ascending and desceding order---#

a = ['abc', 'def', 'avh', 'bbb', 'ccc', 'zed', 'zas']#random list

a.sort()#sorting in ascending order

print('List in ascending order: ', a)#displaying result

a.sort(reverse=True)#sorting in descending order

print('List in descending order: ',a)#displaying result